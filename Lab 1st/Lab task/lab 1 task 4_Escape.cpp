
#include <iostream>     // This library is used to display output on the screen

using namespace std;    // Allows us to use cout easily without std::

int main()              // Program starts running from here
{
    
    cout << "I am in \r electrical department. \n";
    // This line prints a message
    // \r moves the cursor to the beginning of the line
    // \n moves the cursor to the next line

    cout << "I am programming \r enthusiast!";
    // This line prints another message
    // \r again moves the cursor to the start of the line

    return 0;           
    // This line ends the program normally
}


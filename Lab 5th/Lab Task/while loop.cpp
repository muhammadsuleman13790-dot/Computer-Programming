#include <iostream>   // For input and output operations (cout)
using namespace std;  // Allows using cout without writing std::cout

int main()
{
    int i = 1;  // Initialize counter variable to 1

    // Loop runs while i is less than or equal to 10
    while(i <= 10)
    {
        cout << i << endl;  // Print the current value of i
        i++;                // Increment i by 1
    }

    return 0;  // Indicate successful program execution
}
